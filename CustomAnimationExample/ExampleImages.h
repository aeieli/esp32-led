// ========== 简单的星星图标 24x24 ==========
// 生成一个简单的星星图案
void generateStarIcon(uint16_t* buffer, uint16_t color) {
  // 这里简化处理，实际应用中可以用工具转换真实图片
  for (int i = 0; i < 24 * 24; i++) {
    buffer[i] = 0x0000; // 默认黑色背景
  }

  // 在中心绘制一个简单的十字星星形状
  for (int i = 8; i < 16; i++) {
    for (int j = 0; j < 24; j++) {
      buffer[i * 24 + j] = color;  // 横条
      buffer[j * 24 + i] = color;  // 竖条
    }
  }
}

